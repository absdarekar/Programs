package Mathematics;
public class Trignometry
{
    public static void main(String atharv[])
    {
        double m=30,n=60,p=45;
        double x,y,z; 
        x=(22.0/(7.0*180.0))*m;
        y=(22.0/(7.0*180.0))*n;
        z=(22.0/(7.0*180.0))*p;
        System.out.println("The Trignometrical Values are :");
        System.out.println("The value of Sin 30* ="+Math.sin(x));
        System.out.println("The value of Cos 60* ="+Math.cos(y));
        System.out.println("The value of Tan 45* ="+Math.tan(z));
    }
}