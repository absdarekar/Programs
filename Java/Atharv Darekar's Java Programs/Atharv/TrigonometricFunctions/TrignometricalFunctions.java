/**
 * These Functions Return Trigonometric Value Of Given Angle 
 */
package Atharv.TrigonometricFunctions;
public class TrignometricalFunctions
{   
    public static double sin(double in)
    {
        return Math.sin(Math.toRadians(in));
    }

    public static double cos(double in)
    {
        return Math.cos(Math.toRadians(in));
    }

    public static double tan(double in)
    {
        return Math.tan(Math.toRadians(in));
    }
}
